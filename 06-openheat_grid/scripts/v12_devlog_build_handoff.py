from __future__ import annotations

import argparse
import csv
import json
import subprocess
from pathlib import Path
from typing import Any


def parse_args() -> argparse.Namespace:
    """Parse CLI arguments."""

    parser = argparse.ArgumentParser(
        description="Build Chinese handoff and roadmap Markdown docs from devlog CSV matrices."
    )
    parser.add_argument(
        "--config",
        required=True,
        type=Path,
        help="Path to JSON-compatible YAML config declaring inputs and outputs.",
    )
    return parser.parse_args()


def load_config(path: Path) -> dict[str, Any]:
    """Load a JSON-compatible YAML config."""

    with path.open("r", encoding="utf-8") as handle:
        return json.load(handle)


def resolve_path(path_value: str, base_dir: Path) -> Path:
    """Resolve a config path relative to the current worktree."""

    path = Path(path_value).expanduser()
    if path.is_absolute():
        return path
    return (base_dir / path).resolve()


def read_csv(path: Path) -> list[dict[str, str]]:
    """Read a CSV file as dictionaries."""

    if not path.exists():
        return []
    with path.open("r", encoding="utf-8", newline="") as handle:
        return list(csv.DictReader(handle))


def git_value(args: list[str], cwd: Path) -> str:
    """Return a git command value or a stable fallback."""

    try:
        result = subprocess.run(
            ["git", *args],
            cwd=cwd,
            check=False,
            capture_output=True,
            text=True,
        )
    except OSError:
        return "git_unavailable"
    value = result.stdout.strip()
    if result.returncode != 0:
        return f"git_error:{result.stderr.strip()}"
    return value


def md_escape(value: Any) -> str:
    """Escape a value for a compact Markdown table."""

    text = str(value if value is not None else "")
    return text.replace("|", "\\|").replace("\n", "<br>")


def md_table(rows: list[dict[str, Any]], headers: list[str]) -> str:
    """Render a Markdown table."""

    if not rows:
        return "_无记录。_"
    lines = [
        "| " + " | ".join(headers) + " |",
        "| " + " | ".join("---" for _ in headers) + " |",
    ]
    for row in rows:
        lines.append("| " + " | ".join(md_escape(row.get(header, "")) for header in headers) + " |")
    return "\n".join(lines)


def write_text(path: Path, text: str) -> None:
    """Write UTF-8 text, creating parent directories."""

    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text.rstrip() + "\n", encoding="utf-8")


def compact_lane_rows(lanes: list[dict[str, Any]], system: str) -> list[dict[str, Any]]:
    """Return compact lane rows for a system."""

    rows: list[dict[str, Any]] = []
    for lane in lanes:
        if lane["system"] != system:
            continue
        rows.append(
            {
                "lane": lane["lane_id"],
                "status": lane["status"],
                "class": lane["classification"],
                "result": "; ".join(lane.get("quantitative_results", [])[:3]),
                "next": lane["next_action"],
            }
        )
    return rows


def lane_detail_sections(lanes: list[dict[str, Any]], system: str) -> str:
    """Render complete per-lane detail tables."""

    sections: list[str] = []
    for lane in lanes:
        if lane["system"] != system:
            continue
        rows = [
            {"field": "状态", "value": lane["status"]},
            {"field": "分类", "value": lane["classification"]},
            {"field": "分支", "value": lane.get("branch", "")},
            {"field": "目的", "value": lane["purpose"]},
            {"field": "记录命令", "value": "; ".join(lane.get("commands", []))},
            {"field": "配置", "value": "; ".join(lane.get("configs", []))},
            {"field": "脚本", "value": "; ".join(lane.get("scripts", []))},
            {"field": "文档", "value": "; ".join(lane.get("docs", []))},
            {"field": "输出目录", "value": lane["output_dir"]},
            {"field": "关键输出", "value": "; ".join(lane.get("key_outputs", []))},
            {"field": "关键发现", "value": "; ".join(lane.get("key_findings", []))},
            {"field": "量化结果", "value": "; ".join(lane.get("quantitative_results", []))},
            {"field": "接受结论", "value": lane["accepted_conclusion"]},
            {"field": "注意事项", "value": "; ".join(lane.get("caveats", []))},
            {"field": "禁止表述", "value": "; ".join(lane.get("forbidden_claims", []))},
            {"field": "下一步", "value": lane["next_action"]},
            {"field": "重入命令", "value": lane.get("reentry_command", "not_applicable")},
        ]
        sections.append(f"### {lane['lane_id']} {lane['name']}\n\n" + md_table(rows, ["field", "value"]))
    return "\n\n".join(sections)


def filter_rows(rows: list[dict[str, str]], **filters: str) -> list[dict[str, str]]:
    """Filter CSV rows by exact values."""

    filtered = rows
    for key, value in filters.items():
        filtered = [row for row in filtered if row.get(key) == value]
    return filtered


def path_list(config: dict[str, Any], keys: list[str]) -> str:
    """Render configured output paths as Markdown bullets."""

    outputs = config["outputs"]
    return "\n".join(f"- `{outputs[key]}`" for key in keys)


