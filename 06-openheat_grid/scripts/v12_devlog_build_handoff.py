def main() -> None:
    """CLI entrypoint."""

    args = parse_args()
    cwd = Path.cwd()
    config = load_config(args.config)
    outputs = config["outputs"]

    status_rows = read_csv(resolve_path(outputs["status_matrix_csv"], cwd))
    artifact_rows = read_csv(resolve_path(outputs["artifact_matrix_csv"], cwd))
    decision_rows = read_csv(resolve_path(outputs["decision_register_csv"], cwd))
    claim_rows = read_csv(resolve_path(outputs["claim_boundary_csv"], cwd))
    future_rows = read_csv(resolve_path(outputs["future_action_csv"], cwd))
    missing_rows = read_csv(resolve_path(outputs["missing_artifacts_csv"], cwd))

    write_text(
        resolve_path(outputs["status_md"], cwd),
        build_status_doc(config, status_rows, missing_rows),
    )
    write_text(
        resolve_path(outputs["report_md"], cwd),
        build_report_doc(config, status_rows, artifact_rows, missing_rows),
    )
    write_text(
        resolve_path(outputs["full_devlog_doc"], cwd),
        build_main_doc(config, cwd, status_rows, decision_rows, claim_rows, future_rows, missing_rows),
    )
    write_text(
        resolve_path(outputs["new_chat_context_doc"], cwd),
        build_new_chat_context(config),
    )
    write_text(
        resolve_path(outputs["active_dev_board_doc"], cwd),
        build_active_board(config),
    )
    write_text(
        resolve_path(outputs["roadmap_update_doc"], cwd),
        build_roadmap_update(config),
    )
    print(f"handoff_complete docs=6 missing={len(missing_rows)}")


if __name__ == "__main__":
    main()
