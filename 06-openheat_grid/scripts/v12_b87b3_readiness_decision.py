@dataclass(frozen=True)
class ReadinessDecision:
    """Final B8.7b.3 decision summary."""

    status: str
    dsm_status: str
    cdsm_status: str
    grid_status: str
    dem_landcover_status: str
    base_svf_status: str
    overhead_svf_status: str
    overhead_source_status: str
    header_metadata_status: str
    feasibility_headline: str
    audit_headline: str
    next_lane: str
    files_created: list[str]


def index_by_kind(rows: list[dict[str, str]]) -> dict[str, dict[str, str]]:
    """Index CSV rows by source_kind."""
    return {clean(row.get("source_kind")): row for row in rows}


def lock_summary(row: dict[str, str]) -> str:
    """Return compact lock status text."""
    return f"{clean(row.get('lock_status'))} ({clean(row.get('version_status'))})"


def header_status(config: dict[str, Any]) -> str:
    """Return header metadata summary."""
    rows = read_csv_rows(out_path(config, "b87b3_header_metadata.csv"))
    if not rows:
        return "HEADER_NOT_CHECKED"
    ok = sum(1 for row in rows if clean(row.get("header_status")) == "HEADER_OK")
    return f"HEADER_OK={ok}/{len(rows)}"


def overhead_status(config: dict[str, Any]) -> str:
    """Return overhead source status summary."""
    rows = read_csv_rows(out_path(config, "b87b3_overhead_source_inventory.csv"))
    found = [row for row in rows if clean(row.get("supports_overhead_scenario")) == "yes"]
    if not found:
        return "OVERHEAD_SOURCE_MISSING"
    locked = [row for row in found if clean(row.get("version_status")) == "LOCKED_CANONICAL_V10_OVERHEAD_LAYER"]
    return "LOCKED_CANONICAL_V10_OVERHEAD_LAYER" if locked else "OVERHEAD_SOURCE_FOUND_REVIEW"


def feasibility_status(config: dict[str, Any]) -> str:
    """Return compact feasibility headline."""
    rows = read_csv_rows(out_path(config, "b87b3_extraction_feasibility_matrix.csv"))
    base = next((row for row in rows if clean(row.get("check_item")) == "base_scenario_pre_materialization"), {})
    overhead = next((row for row in rows if clean(row.get("check_item")) == "overhead_as_canopy_pre_materialization"), {})
    return f"base={clean(base.get('status'))}; overhead={clean(overhead.get('status'))}"


def decide(config: dict[str, Any]) -> ReadinessDecision:
    """Apply B8.7b.3 decision rules."""
    locks = index_by_kind(read_csv_rows(out_path(config, "b87b3_version_lock_decision.csv")))
    dsm_status = lock_summary(locks["dsm"])
    cdsm_status = lock_summary(locks["cdsm_base_vegetation"])
    grid_status = lock_summary(locks["grid_geometry"])
    base_svf_status = lock_summary(locks["svf_base_full"])
    dem_landcover = f"DEM={lock_summary(locks['dem'])}; landcover={lock_summary(locks['landcover'])}"
    overhead_source = overhead_status(config)
    overhead_svf = lock_summary(locks["svf_overhead"])
    critical_missing = [
        kind
        for kind in ["dsm", "cdsm_base_vegetation", "grid_geometry", "svf_base_full"]
        if "MISSING_CRITICAL_SOURCE" in clean(locks[kind].get("lock_status"))
    ]
    version_ambiguous = False
    if critical_missing:
        status = "B87B3_MISSING_CRITICAL_SOURCE"
        next_lane = "B8.7b.3 source review patch or source recovery"
    elif version_ambiguous:
        status = "B87B3_VERSION_AMBIGUOUS_REQUIRES_REVIEW"
        next_lane = "B8.7b.3 source review patch"
    elif overhead_source == "OVERHEAD_SOURCE_MISSING":
        status = "B87B3_READY_BASE_ONLY_OVERHEAD_BLOCKED"
        next_lane = "B8.7b.5 overhead source recovery"
    else:
        status = "B87B3_SOURCE_LOCK_READY_FOR_MATERIALIZATION"
        next_lane = "B8.7b.4 local-only materialization/pre-extraction package"
    return ReadinessDecision(
        status=status,
        dsm_status=dsm_status,
        cdsm_status=cdsm_status,
        grid_status=grid_status,
        dem_landcover_status=dem_landcover,
        base_svf_status=base_svf_status,
        overhead_svf_status=overhead_svf,
        overhead_source_status=overhead_source,
        header_metadata_status=header_status(config),
        feasibility_headline=feasibility_status(config),
        audit_headline=(
            "PASS: no raster pixel read; no raster write/copy/move/symlink; "
            "no svfs.zip open; no QGIS/SOLWEIG; no manifest/runner; no AOI/B9/WBGT/risk/coupling"
        ),
        next_lane=next_lane,
        files_created=FILES_CREATED,
    )


