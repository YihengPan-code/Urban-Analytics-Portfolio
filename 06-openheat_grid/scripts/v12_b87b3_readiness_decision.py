def write_artifacts(config: dict[str, Any], result: ReadinessDecision) -> None:
    """Write all readiness artifacts."""
    write_projection(config, result)
    write_blockers(config, result)
    write_audit(config)
    write_next_lane(config, result)
    write_reports(config, result)


def run(config_path: Path = DEFAULT_CONFIG) -> ReadinessDecision:
    """Run readiness decision and artifact writing."""
    config = load_config(config_path)
    result = decide(config)
    write_artifacts(config, result)
    return result


def main() -> None:
    """CLI entry point."""
    parser = argparse.ArgumentParser(
        description=(
            "Decide B8.7b.3 source-lock readiness and write reports/prompts/audits; "
            "no staging, commits, raster writes, or QGIS/SOLWEIG."
        )
    )
    parser.add_argument("--config", type=Path, default=DEFAULT_CONFIG)
    args = parser.parse_args()
    print(run(args.config))


if __name__ == "__main__":
    main()
